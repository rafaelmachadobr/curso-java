package fundamentos;

public class inferencia {

	public static void main(String[] args) {
		
		double a = 5.7;
		System.out.println(a);
		
		var b = 8.2;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);
		
		c = "Outro texto";
		System.out.println(c);
	
		int d;
		d = 19;
		System.out.println(d);
	}
}
